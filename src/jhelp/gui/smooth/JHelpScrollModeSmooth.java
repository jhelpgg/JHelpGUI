package jhelp.gui.smooth;

/**
 * Scroll way in {@link JHelpScrollPaneSmooth} and {@link JHelpListSmooth}
 * 
 * @author JHelp
 */
public enum JHelpScrollModeSmooth
{
   /** Scroll horizontally and vertically (Not recommended for {@link JHelpListSmooth}) */
   SCROLL_BOTH,
   /** Scroll horizontally only */
   SCROLL_HORIZONTAL,
   /** Scroll vertically only */
   SCROLL_VERTICAL;
}